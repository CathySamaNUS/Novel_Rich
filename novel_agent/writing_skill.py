DEFAULT_SHARED_SKILL = """
# 通用写作 Skill

## 章节目标
- 每一章都必须完成一个清晰的推进任务：关系变化、利益变化、局势变化至少发生一种。
- 章节不能只是解释背景，必须让角色通过行动与选择推动剧情。
- 每章结尾要留下明确的情绪钩子、信息钩子或风险钩子。

## 场景设计
- 开场尽量晚一点进入冲突，避免冗长铺垫。
- 一个场景只承担 1-2 个核心功能，不要同时塞太多信息。
- 每个 beat 都要写清楚谁想得到什么、谁在阻拦、局面如何变化。
- 场景结束时要和开场相比发生实质变化，不能原地踏步。

## 人物与关系
- 人物动机要和长期人设、当前状态一致。
- 关系推进优先通过对话、试探、误解、交换条件、失控反应来体现，不要靠旁白硬讲。
- 强关系戏里要同时存在情绪拉扯和现实利益，避免只剩空泛暧昧。
- 反派或阻力角色每次出场都应带来真实代价或威胁。

## 商业网文节奏
- 尽早抛出压力，不要把有效冲突拖到章节后半段。
- 信息揭示遵循“半给半藏”，既让读者看懂当前局面，又保留继续追读的空间。
- 爽点要建立在压迫、误判、代价之后，避免无铺垫的直接打脸。
- 支线要服务主线，不要写与当前核心矛盾无关的散戏。

## 语言与表达
- 少写重复解释、总结性复述和空泛抒情。
- 对话要带目的性和潜台词，避免所有角色说话一个腔调。
- 描写优先服务情绪、关系和局势，不堆砌无功能细节。
- 能用动作和反应呈现的内容，就不要用抽象判断直接宣布。

## 连续性纪律
- 严格延续既有人名、称呼、身份、时间线、空间位置和伏笔状态。
- 长期设定不要轻易改；会变化的内容应落在动态人物状态和剧情记忆里。
- 新增设定必须能和既有内容兼容，不能为当前一章临时硬造规则。

## 重生成纪律
- 如果收到重生成反馈，先改章节打法，再改句子表层。
- 必改项优先级高于文风润色；禁止项绝不能重复出现。
- 如果旧版本某段戏本身失效，应直接换场景或换推进方式，不要勉强修补。
""".strip()

